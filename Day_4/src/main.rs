fn main() {
    // Integer variables
    let age: i32 = 30;
    let height: u64 = 180;

    // Float variable
    let weight: f64 = 75.5;

    // Boolean variable
    let is_rust_fun: bool = true;

    // Char variable
    let first_initial: char = 'J';

    // Printing variables
    println!("Age: {}", age);
    println!("Height: {} cm", height);
    println!("Weight: {} kg", weight);
    println!("Is Rust fun?: {}", is_rust_fun);
    println!("First initial: {}", first_initial);
}
